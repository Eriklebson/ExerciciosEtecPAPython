diaSemana = int(input("Informe o numero do dia da semana de 1 a 7:"))

if (diaSemana == 1):
    print("Hoje é segunda-feira!")
elif (diaSemana == 2):
    print("Hoje é Terça-feira!")
elif (diaSemana == 3):
    print("Hoje é Quarta-Feira!")
elif (diaSemana == 4):
    print("Hoje é Quinta-feira!")
elif (diaSemana == 5):
    print("Hoje é Sexta-Feira!")
elif (diaSemana == 6):
    print("Hoje é Sábado!")
elif (diaSemana == 7):
    print("Hoje é Domingo!")
else:
    print("Numero invalido!")
    
